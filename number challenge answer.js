
function sumThreeNumbers(num1, num2, num3) {
    return num1 + num2 + num3;
}

function differenceOfTwoNumbers(num1, num2) {
    return num1 - num2;
}

function productOfTwoNumbers(num1, num2) {
    return num1 * num2;
}

function averageOfTwoNumbers(num1, num2) {
    return (num1 + num2) / 2;
}

console.log("Sum:", sumThreeNumbers(40, 37, 2));
console.log("Difference:", differenceOfTwoNumbers(10, 40));
console.log("Product:", productOfTwoNumbers(67, 78));
console.log("Average:", averageOfTwoNumbers(80, 12));

